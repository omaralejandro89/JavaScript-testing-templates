var animal = prompt("Insert animal", "");

switch (animal) {
    case "cat":
        alert("meow");
        break;
    case "dog":
        alert("woof");
        break;
    case "horse":
        alert("whinny")
        break;
    default:
        alert("animal");
        break;
}